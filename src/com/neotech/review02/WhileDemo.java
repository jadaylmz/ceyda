package com.neotech.review02;

public class WhileDemo {

	public static void main(String[] args) {

		String greeting = "Good Morning";
		int count = 0;
		while (count < 6) {
			System.out.println(count + " " + greeting);
			count++;
		}
		
		System.out.println("======================");
		
		
		
	}

}
